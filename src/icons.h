class IconManager {
public:    
    enum IconType { LUX, BATTERY, TEMPERATURE, ORIENTATION, LAN, SD_CARD, CLOCK, POWER, NONE };

IconManager(Adafruit_GFX* display, IconType type, int16_t x, int16_t y, int idMenu, const char* title) 
        : _display(display), _type(type), _x(x), _y(y), _idMenu(idMenu), _title(title), _currentIndex(-1) {}

    // Cambia la posición del icono
    void setPosition(int16_t x, int16_t y) {
        _x = x;
        _y = y;
    }

  void update(int newIndex, bool isSelected) {
      // 1. Verificación de cambios: Solo actúa si cambia el icono O si cambia la selección
//      if (newIndex == _currentIndex && isSelected == _lastSelected) { return;  }

      // 2. Definir colores según la selección (Modo Invertido)
      // Si está seleccionado: Fondo Blanco, Icono Negro
      // Si no está seleccionado: Fondo Negro, Icono Blanco
      isSelected =false;
      uint16_t bgColor   = isSelected ? SSD1306_WHITE : SSD1306_BLACK;
      uint16_t iconColor = isSelected ? SSD1306_BLACK : SSD1306_WHITE;

      // 3. Obtener el bitmap antes de borrar para evitar dejar el hueco vacío si falla
      const unsigned char* bitmap = getBitmap(_type, newIndex);
      
      if (bitmap != nullptr) {
          // Borrar fondo con el color de selección
          _display->fillRect(_x, _y, 16, 16, bgColor);
         
          // Dibujar el icono encima con el color contrastado
          _display->drawBitmap(_x, _y, bitmap, 16, 16, iconColor);
          
          // Actualizar estados
          _currentIndex = newIndex;
          _lastSelected = isSelected;
      }
  }
    
    // Getters
    IconType getType() const { return _type; }
    int getIndex() const { return _currentIndex; }
    const char* getTitle() { return _title; }
    int getIdMenu() { return _idMenu; }

private:
    Adafruit_GFX* _display;
    IconType _type;
    int16_t _x, _y;
    int _currentIndex;
    int _lastSelected;
    int _idMenu; 
    const char* _title; 


    const unsigned char* getBitmap(IconType type, int index) {
        switch (type) {
            case POWER:
                if (index == 1) return icon_pwr_40;
                if (index == 2) return icon_pwr_60;
                if (index == 3) return icon_pwr_80;
                if (index == 4) return icon_pwr_100;
                return icon_pwr_20; // Por defecto el primero (0 o cualquier otro)

            case BATTERY:
                if (index == 1) return icon_bat_25;
                if (index == 2) return icon_bat_50;
                if (index == 3) return icon_bat_75;
                if (index == 4) return icon_bat_100;
                if (index == 5) return icon_cargando;
                if (index == 6) return icon_enchufado;
                if (index == 7) return icon_error_bateria;
                return icon_bat_critica; // Por defecto el primero (0)

            case TEMPERATURE:
                if (index == 1) return icon_temp_25;
                if (index == 2) return icon_temp_50;
                if (index == 3) return icon_temp_75;
                if (index == 4) return icon_temp_100;
                return icon_temp_0; // Por defecto el primero (0)

            case ORIENTATION:
                if (index == 1) return icon_bac_45deg;
                if (index == 2) return icon_bac_horizontal;
                return icon_bac_vertical; // Por defecto el primero (0)
            
            case LAN:
                if (index == 1) return icon_lan_connected;
                if (index == 2) return icon_lan_activity;     
                if (index == 3) return icon_lan_error;    
                return icon_lan_disconnected; // Por defecto el primero (0)
                
            case SD_CARD:
                if (index == 1) return icon_sd_ok;    
                if (index == 2) return icon_sd_write; 
                if (index == 3) return icon_sd_error; 
                return icon_sd_none; // Por defecto el primero (0)

            case CLOCK:
                if (index == 1) return icon_clock_winter;
                if (index == 2) return icon_clock_summer;
                return icon_clock_error; // Por defecto el primero (0)

            default: 
                return icon_clock_error; // Fallback total si el tipo no existe
        }
    }

};

